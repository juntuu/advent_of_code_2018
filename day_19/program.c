typedef unsigned long T;

T program(T *r) {
	r[2] = r[2] + 2;
	r[2] = r[2] * r[2];
	r[2] = 19 * r[2];
	r[2] = r[2] * 11;
	r[5] = r[5] + 3;
	r[5] = r[5] * 22;
	r[5] = r[5] + 3;
	r[2] = r[2] + r[5];

	if (r[0]) {
		r[5] = 27;
		r[5] = r[5] * 28;
		r[5] = 29 + r[5];
		r[5] = 30 * r[5];
		r[5] = r[5] * 14;
		r[5] = r[5] * 32;
		r[2] = r[2] + r[5];
		r[0] = 0;
	}

	r[1] = 1;
	do {
		r[4] = 1;
		do {
			r[5] = r[1] * r[4];
			r[5] = r[5] == r[2];

			if (r[5]) r[0] = r[1] + r[0];

			r[4] = r[4] + 1;
			r[5] = r[4] > r[2];

		} while (!r[5]);
		r[1] = r[1] + 1;
		r[5] = r[1] > r[2];

	} while (!r[5]);

	return r[0];
}

